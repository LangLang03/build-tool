android_i18n_get() {
    local key="$1"
    local -n strings_ref
    
    if [[ "$ANDROID_I18N_LANG" == "zh" ]]; then
        strings_ref=ANDROID_I18N_ZH
    else
        strings_ref=ANDROID_I18N_EN
    fi
    
    if [[ -n "${strings_ref[$key]:-}" ]]; then
        echo "${strings_ref[$key]}"
    else
        echo "$key"
    fi
}

android_i18n_printf() {
    local key="$1"
    shift
    local msg
    msg=$(android_i18n_get "$key")
    printf "$msg" "$@"
}

