i18n_set_lang() {
    local lang="$1"
    case "${lang,,}" in
        zh|zh-cn|zh-hans|chinese)
            I18N_CURRENT_LANG="zh"
            ;;
        en|en-us|english|*)  
            I18N_CURRENT_LANG="en"
            ;;
    esac
}

i18n_get() {
    local key="$1"
    local -n strings_ref
    
    if [[ "$I18N_CURRENT_LANG" == "zh" ]]; then
        strings_ref=I18N_STRINGS_ZH
    else
        strings_ref=I18N_STRINGS_EN
    fi
    
    if [[ -n "${strings_ref[$key]:-}" ]]; then
        echo "${strings_ref[$key]}"
    else
        echo "$key"
    fi
}

i18n_printf() {
    local key="$1"
    shift
    local msg
    msg=$(i18n_get "$key")
    printf "$msg" "$@"
}

i18n_echo() {
    local key="$1"
    shift
    local msg
    msg=$(i18n_get "$key")
    echo -e "$msg"
}

i18n_init() {
    _i18n_init_strings
    
    if [[ -n "$LANG" ]]; then
        if [[ "$LANG" == *"zh"* ]]; then
            i18n_set_lang "zh"
        fi
    fi
}

i18n_init

